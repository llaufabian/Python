# Operadores Aritméticos
valorA = float(input("Digite o valor de A: "))
valorB = float(input("Digite o valor de B: "))
# Soma
soma = valorA + valorB
print(f"A soma de {valorA} e {valorB} é {soma}")
# Subtração
subtracao = valorA - valorB
print(f"A subtração de {valorA} e {valorB} é {subtracao}")
# Multiplicação
multiplicacao = valorA * valorB
print(f"A multiplicação de {valorA} e {valorB} é {multiplicacao}")
# Divisão
divisao =  valorA / valorB
print(f"A divisão de {valorA} e {valorB} é {divisao}")
# Divisão Inteira
divisao_inteira = valorA // valorB
print(f"A divisão inteira de {valorA} e {valorB} é {divisao_inteira}")
# Resto da Divisão
resto_divisao = valorA % valorB
print(f"O resto da divisão de {valorA} e {valorB} é {resto_divisao}")
# Potenciação
potenciacao = valorA ** valorB
print(f"A potenciação de {valorA} e {valorB} é {potenciacao}")